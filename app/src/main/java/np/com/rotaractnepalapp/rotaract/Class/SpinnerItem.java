package np.com.rotaractnepalapp.rotaract.Class;

public class SpinnerItem {
    private String  mClubName;
    private int mClubImage;

    public SpinnerItem(String mClubName, int mClubImage) {
        this.mClubName = mClubName;
        this.mClubImage = mClubImage;
    }

    public String getmClubName() {
        return mClubName;
    }

    public int getmClubImage() {
        return mClubImage;
    }
}
