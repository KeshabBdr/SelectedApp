package np.com.rotaractnepalapp.rotaract.Class;

public class Club8Class {
    private int images;
    private String title;

    public Club8Class(){

    }

    public Club8Class(int images, String title) {
        this.images = images;
        this.title = title;
    }

    public int getImages() {
        return images;
    }

    public void setImages(int images) {
        this.images = images;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
